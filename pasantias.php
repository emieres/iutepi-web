		<h4 class="titulo">COORDINACION DE PASANTÍAS</h4><hr>

		<div class="titulo3">
			<p>La Coordinación de Pasantías le da la más cordial bienvenida al último semestre de formación profesional como Técnico Superior Universitario en la especialidad elegida.</p>

			<p>La Coordinación de Pasantías se encuentra ubicada en el Segundo Piso, Nave A. Contamos con un grupo de trabajo altamente dispuestos a atender sus requerimientos en esta etapa final de la carrera.</p>

			<p>El horario de atención al público es de 08:00 AM a 09:00 PM.</p>

			<p>La pasantía es una unidad curricular obligatoria del plan de estudios de la carrera cursada, con una carga crediticia de cinco unidades (5 UC). Por su condición de TSU, tiene una duración de ocho (8) semanas tomando en cuenta una jornada laboral de 8 horas; o dieciséis (16) semanas, si realizas medio tiempo de la Jornada Laboral correspondiente.</p>
		</div>

		<p class="titulo3">Para cursar las pasantías profesionales, tiene la oportunidad de elegir entre 2 modalidades:</p>

		<ul class="titulo3">
			<li>Pasantía Profesional.</li>
			<li>Acreditación de Pasantías.</li>
		</ul>

		<p class="titulo4">PASANTÍA PROFESIONAL</p>

		<div class="titulo3">
			<p>Por su condición de TSU, tiene una duración de ocho (8) semanas tomando en cuenta una Jornada Laboral de 8 horas; o dieciséis (16) semanas, si realizas medio tiempo de la Jornada Laboral.</p>

			<p>Para optar a la realización de la pasantía, debes haber aprobado el total de las unidades de crédito previsto en el plan de estudio de la carrera.</p>

			<p>Para inscribirte, debes realizar los siguientes pasos:</p>

			<ul>
				<li>Haber formalizado el proceso de inscripción (por el Dpto. de Caja y por Control de Estudios).</li>
				<li>Llenar el Formato de Inscripción de pasantías; una vez llenado, anéxale una fotografía reciente y sácale una copia a la planilla.</li>
				<li>Entrega el original y la copia de la planilla a la Coordinación de Pasantías.</li>
				<li>En la Coordinación, gestionaran la carta de presentación o de postulación según sea el caso. (En la carta de presentación se presenta la solicitud a la Empresa de su condición de pasante, con el propósito de gestionar sus labores en ella. La carta de postulación se emite, cuando la empresa ha autorizado sus pasantías profesionales).</li>
				<li>Una vez sea emitida la carta de postulación, la Coordinación se la entregará conjuntamente con la planilla de FUNDEI, que es la Fundación encargada de velar por el enlace institucional de los pasantes. La Carta de Postulación y la planilla de FUNDEI debe ser entregada a la Organización donde iniciarás las pasantías.</li>
			</ul>

			<p>Como pasante, debes solicitar a la Organización donde realizarás la pasantía lo siguiente:</p>

			<ul>
				<li>Copia de la carta de postulación.</li>
				<li>Firma de la Planilla de FUNDEI.</li>
				<li>Carta de Aceptación (en la cual debe especificar el lapso de la pasantía y el Tutor Institucional, quien es la persona que le supervisará y evaluará la pasantía).</li>
			</ul>

			<mark class="titulo2">Nota: Los 2 primeros recaudos debes llevarlo de inmediato a la Coordinación de Pasantía para que le asignen el Tutor Académico. La carta de aceptación, en el tiempo que la organización se la entregue. </mark><br><br>

			<p>Una vez que culmine su período de pasantías, solicita la "Carta de Culminación", lleve una copia a la Coordinación de Pasantías.</p>

			<p>La Coordinación de Pasantías, conjuntamente con la Coordinación de su Especialidad, realizará las revisiones y evaluaciones correspondientes (Ver Cronograma de Inscripción de Pasantías)</p>

		</div>

		<p class="titulo4">ACREDITACION DE PASANTÍAS</p>

		<p class="titulo3">Para poder solicitar la acrediatación de pasantías debes cumplir con el perfil según tu especialidad.</p>

		<p class="titulo4">PERFIL ADMINISTRACIÓN INDUSTRIAL</p>

		<ul class="titulo3">
			<li>Más de un (1) año de experiencia en el área que cursa.</li>
			<li>Participar en la administración contable, financiera y operativa de una empresa industrial, comercial y de servicios.</li>
			<li>Apoyar la aplicación de métodos para dirigir, controlar y supervisar las actividades productivas, a través de sistemas de planificación, estructuración, organización y coordinación de los recursos disponibles.</li>
			<li>Dirigir y/o ejecutar las decisiones administrativas de la gerencia de planta y de la administración.</li>
			<li>Documentar manuales de normas y procedimientos bajo el enfoque de los estándares de calidad más exigentes.</li>
		</ul>

		<p class="titulo4">PERFIL ANÁLISIS DE SISTEMAS</p>

		<ul class="titulo3">
			<li>Más de un (1) Año de experiencia en el área que cursa.</li>
			<li>Desarrollar a nivel lógico cualquier programa científico administrativo.</li>
			<li>Manejar paquetes de aplicaciones como: nómina, inventario, hojas de cálculo.</li>
			<li>Analizar, diseñar y programar sistemas de computación.</li>
			<li>Analizar, evaluar e implementar sistemas operativos y sistemas de aplicaciones.</li>
			<li>Preparar estudios comparativos de Hardware y Software para proponer mejores soluciones.</li>
		</ul>

		<p class="titulo4">PERFIL ELECTRONICA</p>

		<ul class="titulo3">
			<li>Más de un (1) Año de experiencia en el área que cursa.</li>
			<li>Participar en la instalación y modificación de equipos y Sistemas Electrónicos.</li>
			<li>Realizar prototipos de Circuitos Electrónicos.</li>
			<li>Ejecutar planes de mantenimiento preventivo y correctivo.</li>
			<li>Detectar y corregir fallas.</li>
			<li>Efectúar actividades de ajuste y puesta a punto de parámetros de control de máquinas, equipos y herramientas.</li>
			<li>Efectúar programación de dispositivos (Autómatas, microcontroladores/centrales).</li>
			<li>Verificar el funcionamiento de dispositivos electrónicos/eléctricos y de control.</li>
			<li>Realizar lectura e interpretación de planos y esquemas eléctricos.</li>
			<li>Manejar de catálogos y manuales técnicos de fabricantes.</li>
		</ul>


		<div class="titulo3">
			<p>Si cumples con los requisitos del perfil de la Especialidad, puedes optar por la Acreditación de Pasantías.</p>

