			<ul>
				<li>Haber formalizado el proceso de inscripción (por el Dpto. de Caja y por Control de Estudios).</li>
				<li>Llenar el Formato de Inscripción de acreditación; una vez llenado, anéxale una fotografía reciente y sácale una copia a la planilla (recuerda que es por ambos lado).</li>
				<li>Entrega el original y la copia de la planilla a la Coordinación de Pasantías con los siguientes recaudos:
					<ul>
						<li>Constancia de trabajo (Actual y anteriores) donde se especifique fecha de ingreso/egreso; según sea el caso.</li>
						<li>Descripción del Cargo Actual (Firmada y sellada por la Empresa).</li>
						<li>Resumen curricular vigente.</li>
					</ul>
				</li>
			</ul>

			<p>La Coordinación de Pasantías, conjuntamente con la Coordinación de su Especialidad, realizará las revisiones y evaluaciones correspondientes (Ver Cronograma de Inscripción de Pasantías).</p>

