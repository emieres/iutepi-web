		<p class="titulo4">PERFIL DEL EGRESADO</p>

		<div class="titulo3">
			<p>Con el propósito de cumplir con los objetivos planteados, se pretende que el estudiante adquiera conocimientos formales sobre el Pensamiento Sistémico, los Cambios de Escenarios desde la óptica de la Dinámica de Sistemas, los Sistemas de Información Gerencial y al mismo tiempo adquiera experiencia en la aplicación de los mismos, por lo que será necesario que participe activamente en proyectos de aplicación durante el período de estudios. A tal efecto, la organización de los recursos estará orientada a fomentar el trabajo en equipo y desarrollar la capacidad de presentación y comunicación de ideas.</p>

			<p>El profesional egresado del Programa de Postgrado en la Especialidad Técnica en GERENCIA DE LA INFORMACION, estará en capacidad de diseñar desarrollar actividades en relación con la formulación y ejecución de proyectos a programas, equipos, procedimientos y recursos humanos involucrados en función de la producción y las operaciones en la empresa moderna.</p>

			<p>Asimismo estará en capacidad de entender las vías de acción apropiadas para optimizar los resultados, referidos a reemplazo de equipos, formulación y condiciones de funcionamiento del sistema productivo o de servicios con procedimientos basados en criterios técnicos.</p>

			<p>El egresado obtendrá la formación de un profesional capaz de interpretar las decisiones que emanen de las gerencias superiores y ejecutarlas, desarrollando sus conocimientos, destrezas y habilidades para hacerlo más efectivo y eficiente en el proceso de ejecución de decisiones en el campo administrativo y técnico y soportándolas en herramientas de tecnología de la información.</p>

			<p>Nuestros egresados estarán capacitados para ejecutar, controlar y evaluar de forma sistemática y de manera integral todos los procesos de decisiones y actividades administrativas relacionadas con los Servicios, la Producción, apoyándose para ello de medios automatizados, que le sirvan de soporte a las actividades relacionadas con Administración de la Producción, Comercialización, y de prestación de servicios a las pequeñas, medianas y grandes Organizaciones, tanto Públicas como Privadas, de acuerdo al desarrollo socioeconómico de la región y del país.</p>

			<p>El participante durante su participación en el programa y su formación académica obtendrá una serie de conocimientos sobre la realidad económica y social de la Nación, que le permitirá una amplia visión de la problemática del medio, vinculando su profesión a los dinámicos cambios que están produciéndose en Venezuela y el mundo.</p>
