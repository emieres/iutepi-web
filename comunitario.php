		<p class="titulo4">DEFINICIÓN DE SERVICIO COMUNITARIO</p>

		<div class="titulo3">
			<p>De acuerdo al <strong>Artículo #4</strong>, se entiende por Servicio Comunitario, la actividad que deben desarrollar en las comunidades los estudiantes de educación superior que cursen estudios de formación profesional, aplicando los conocimientos científicos, técnicos, culturales, deportivos y humanísticos adquiridos durante su formación académica, en beneficio de la comunidad, para cooperar con su participación al cumplimiento de los fines del bienestar social, de acuerdo con lo establecido en la Constitución de la República Bolivariana de Venezuela y en esta Ley.  El servicio comunitario se lleva a cabo mediante una planificación que comprende los ejes de investigación participativa y de sensibilización y creación comunitaria, a través del cual se integran las diversas áreas académicas del ciclo profesional.</p>

			<p>Los servicios comunitarios, son beneficios para la comunidad y es una actividad que deben cumplir los estudiantes de educación superior, quienes serán los prestadores de estos servicios, siempre y cuando hayan cumplido al menos con el cincuenta por ciento (50%) del total de la carga académica de la carrera.</p>

			<p>La realización del SCE,  tiene una duración de 120 horas las cuales deben cumplirse en un lapso no menor de tres meses; los estudiantes deben recibir información oportuna de los proyectos.</p>
			<p>La guía de cómo elaborar los Proyectos de SCE y los formatos se pueden descargar desde el menu lateral en el area de descargas: vaya a la opcion <span class="titulo2">DESCARGAS-SERVICIO COMUNITARIO</span> <a class="titulo4" href="descargas.php">Link AQUI</a></p>
		</div>

		<p class="titulo4">MISIÓN</p>

		<p class="titulo3">Promover, orientar y organizar la incorporación del Servicio Comunitario obligatorio de los estudiantes de: Administración Industrial, Análisis de Sistemas y Electrónica, con el fin de contribuir con la formación integral del estudiante, a través del desarrollo de actividades en las comunidades, aplicando los conocimientos adquiridos durante su formación académica, en beneficio de la comunidad, para cooperar con su participación en la disminución de los problemas que la afectan, desarrollando alternativas comunicacionales que contribuyan a solucionarlos, respondiendo así, al compromiso que se tiene con la sociedad.</p>

		<p class="titulo4">VISIÓN</p>

		<p class="titulo3">De acuerdo a los fines de la Ley de Servicio Comunitario y del Reglamento Interno de Servicio Comunitario Estudiantil del Instituto Universitario de Tecnología para la Informática (IUTEPI), la Coordinación de Servicio Comunitario se constituye en una instancia para la formación integral del estudiante, que permite fomentar la solidaridad y el compromiso con la comunidad como norma ética y ciudadana; hacer un acto de reciprocidad con la sociedad; enriquecer la actividad de educación superior, a través del aprendizaje servicio, con la aplicación de los conocimientos adquiridos durante la formación académica; con la comunidad, para contribuir al desarrollo de la sociedad venezolana, y formar, a través del aprendizaje servicio, el capital social en el país.</p> <br><br>
